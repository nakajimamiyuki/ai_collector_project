# Node 4: reflect —— 决策"信息够不够，要不要再搜一轮"
# ----------------------------------------------------------------------
REFLECT_SYSTEM = """你是一个求职 Agent 的反思节点。给定当前已召回的岗位列表，决定是否需要再搜一轮。

判定原则：
1. 高质量岗位 ≥ 5 条 → 直接收工（"done"）
2. 高质量 < 5 条但还有未尝试的关键词 → 再搜一轮（"retry"），给出新的 keywords/cities
3. 已经尝试 ≥ 3 轮 → 强制收工（"done"），不再无限循环

只返回 JSON：
{"decision": "done" | "retry",
 "reason": "<一句话理由>",
 "next_keywords": [...] (仅 retry 时),
 "next_cities":   [...] (仅 retry 时，可空)}
"""


